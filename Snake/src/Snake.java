public class Snake {
	private int x;
	private int y;
	/**
	 * 
	 */
	public Snake(int x, int y) {
		this.x = x;
		this.y = y;
	}
	/**
	 * @return the x
	 */
	public int getX() {
		return x;
	}
	/**
	 * @return the y
	 */
	public int getY() {
		return y;
	}
	/**
	 * @param x the x to set
	 * @param y the y to set
	 */
	public void setPos(int x, int y) {
		this.x = x;
		this.y = y;
	}

}
